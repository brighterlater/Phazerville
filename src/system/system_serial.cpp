#include "system/system_serial.h"

#include <cstdarg>

#include "system/t4.h"

namespace serial {

void wait_available(uint32_t ms)
{
  do {
    auto start = millis();
    while (!Serial && millis() - start < ms) {}
  } while (0);
}

void println(const char *fmt, ...)
{
  char buf[128];
  va_list args;
  va_start(args, fmt);
  vsnprintf(buf, sizeof(buf), fmt, args);
  va_end(args);
  Serial.print(buf);
}

}  // namespace serial
